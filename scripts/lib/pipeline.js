/**
 * scripts/lib/pipeline.js — 9 步主体流水线 prompt 装订
 *
 * 套 SPEC-interpretation.md §五 Step 3-9 + §七 自评 5/4/3 分制
 *
 * 反元规则 prompt 描述从 interpretation-rules.js 统一生成，
 * 与 self-check-lite.js 共享单一数据源。
 */

import { antiMetaPromptBlock } from './interpretation-rules.js'

/**
 * 装订 9 步主体流水线的 prompt
 * @param {{sourceText: string, condition: object, specBundle: {specInterpretation: string, general: string, shuSpecial: string, catalog: string}}} opts
 * @returns {string} 完整 prompt
 */
export function buildPipelinePrompt({ sourceText, condition, specBundle }) {
  return `# 9 步主体流水线（套 SPEC-interpretation.md §五 Step 3-9 + §七）

## 强装载 5 份规范（已通读）

### 1. SPEC-interpretation.md
${specBundle.specInterpretation}

### 2. general.md
${specBundle.general}

### 3. 术数专项
${specBundle.shuSpecial}

### 4. catalog.md
${specBundle.catalog}

### 5. source.md（待解读原文）
${sourceText}

## 原文体检报告

- 模式：${condition.模式}
- 案例：${condition.案例}
- 注家：${condition.注家}
- 异文：${condition.异文}
- 脱漏：${condition.脱漏}
- 超长：${condition.超长}

## 9 步执行清单

### Step 3：内容结构梳理
- 拆段、标注家、分类理论与案例
- 提取二级标题（**反机械化规则**：禁 source 分层标签作标题；标题从原文关键词中抓取）
- 套 §六 异常场景策略

### Step 4：逐段引用 + 表层解读
- 套 §2.1 模板：一引一解
- 引文必须 \`>\` 块引用 + 完整整句
- 术语当场释义融入语言（**禁独立【白话】行**）

### Step 5：案例 / 分歧处理
- 有案例：套 §2.2 案例模板
- 流派分歧：客观陈列，不判对错

### Step 6：深化洞见（按需）
- LLM 自判：确有深层意蕴时撰，无则不强写
- 允许范围见 §一.2 §2

### Step 7：图解补充（按需）
- 套 §2.4 必用 / 禁用规则
- 必用场景：多级逻辑判断 / 流派对比 / 体系脉络
- 禁用场景：纯文字释义 / 装饰性配图

### Step 8：自评合规分（0-5）— **仅 LLM 内部评估，不写入 interpretation.md 正文**
- 致命错误 0 + 格式错误 0 + 内容检查 0 = 5 分
- 致命错误 0 + 格式错误 0 + 内容检查 ≤ 1 = 4 分
- 否则 ≤ 3 分（强制重写）
- **本步骤的 5/4/3 分制评估仅用于 LLM 内部决策（是否重写），绝不以任何形式写进 interpretation.md**
- **禁止在解读正文中出现 "## 自评 / ## 合规分 / ## 致命错误 / ## 格式错误 / ## 内容检查" 等章节**
- **禁止 "无 X、无 Y" 形式的元自我断言列表**
- **禁止 ✓/✗/致命错误（N 项）/全部通过 等自评标记**

### Step 9：输出最终文件
- 自评 ≥ 4 才输出
- 文件结构：见 skeleton.md

${antiMetaPromptBlock()}

## 跨篇关联硬规则（套 §一.2 ❌项 5）

**禁止**无前置跨篇读取依据时的具体跨篇断言，包括但不限于：
- 「前数篇论 X、Y、Z」（具体篇名或主题枚举）
- 「上承 / 下启 / 前承 / 后启」（具体跨篇方向定位）
- 「第 X 篇 / 第 X 章」（具体位置标识）
- 「本篇与第 X 章呼应 / 互参 / 相对」（具体跨篇呼应）
- 「后文'论 X'篇当互参」（指定后续篇名作互参指引）
- 跨书引述（如「《滴天髓征义》卷 X」），除非本篇原文有直接引述

**唯一允许的笼统表述**：
- 「本书论 X 一系……」
- 「子平之通论 / 子平之大纲 / 子平之常法」
- 「格局定法 / 命理通则」等不指定具体篇名的体系性表述
- 「此论在全书之位置 / 此篇所论属 X 范畴」（不指定前后篇）

**改写方向**：用体系性、范畴性表述替代具体篇名呼应。

## 标题反机械化硬规则（套 §一.1 反机械化规则）

**禁止**二级标题使用 source 分层标签：
- 「原注申说」「原注详解」「原注释义」
- 「原文第一段」「原文首段」「原文末段」
- 「第一段 / 第二段 / 段一 / 段二」
- 任何以"原注""原文""段"开头的机械翻译
- **任何以"X X之部""第 X 项""X X总说"机械枚举 source 平铺层次的标题**

**正确做法**：
- 从原文关键词中提炼标题（如"月令为取用之纲维""扶抑之正轨""财旺身弱用印"）
- 用理论概念名（如"曲直仁寿格""官印相生")
- 用问题或论点（如"阴阳顺逆之理""墓库冲与不冲"）

## 篇章结构硬规则（按 source.md 形态分两类组织）

### A. 论理体（source.md 为理论条目罗列，如"天干篇""地支篇""五行篇""强弱篇""六神篇"等基础理论篇）

**禁止**按 source 平铺层次（如"甲/乙/丙/丁/戊/己/庚/辛/壬/癸"逐干分章、逐项重述）。**必须**按理论点重组为 5-9 个二级标题，例如天干篇推荐结构：

1. 阴阳五行总纲（十天干之阴阳五行归属）
2. 十二长生之阳顺阴逆（十天干十二长生对照表 + 阳顺阴逆之理）
3. 五合与合化（甲己/乙庚/丙辛/丁壬/戊癸五合之理 + 化气条件）
4. 生克冲合之体用（十干生克冲合之体用辨析）
5. 六类喜忌之病药体系（强/弱/中强/中弱/太旺/不及六类 + 喜忌原则）
6. 案例体例（10 干 × 4 例合计 40 例之解读体例与共性）
7. 天干之统会（合天干篇于全书之位置 / 通往地支篇之枢纽）

**类推**：地支篇→按"阴阳五行/藏干本气中气余气/六合六冲/三合三会/六害/自刑/合刑冲害并见之理"重组；五行篇→按"五行名义与运行/五行体性/五行六类与喜忌/五行四季休旺/五行统会"重组；强弱篇→按"强弱判则/身强之三等/身弱之三等/喜忌扶抑/强弱之统会"重组；六神篇→按"六神立名之本/六神之能力/六神之四利四弊/六神之喜忌/六神统会"重组。

### B. 实战案例体（source.md 为多命造罗列 + 命主点评，如"应运篇上""应运篇下""评断篇""起例问答"等）

**每造一个独立 H2**（如 "## 蒋介石"、"## 陆姓乾命"、"## 潘姓坤命"），**H2 之下挂固定 #### 子节**：

#### 标准 4 子节版（应运篇上下、起例问答）
每造严格按以下 4 个 #### 子节展开：
1. **#### 格局**（此造之格局判定：日主强弱 / 所取用神 / 属外格或正格）
2. **#### 用神**（所取用神之配伍与喜忌）
3. **#### 大运**（行运吉凶之逐年推演 + 史实验证）
4. **#### 小结**（此造之断语 + 命主命运之总评）

#### 评断篇 8 步版
评断篇五案例（陆姓乾命 / 潘姓坤命 / 王姓坤命 / 詹姓乾命 / 陈姓乾命）**每案 8 个 #### 子节**（不分先后），按原文章节顺序：
1. **#### 强弱**  2. **#### 格局**  3. **#### 用神**  4. **#### 喜忌**  5. **#### 岁运**  6. **#### 六亲**  7. **#### 性情**  8. **#### 事业**

**禁止**主题聚类（"格局与用神总诀""身强用财与身强用杀"等合并多造的主题式 H2）。**禁止**综评式覆盖。

### C. 论命通论体（source.md 为通论条文，如"起例问答""格局篇""外格篇""运限篇""月建篇""流年篇"等）

按独立理论点切分章节，标题从原文关键词中提炼。**禁止**截断式处理——若 source 较长则使用 #### 子节分段（§六.6），保持全文覆盖。

## H1 标题硬规则

**禁止**文件首行出现任何形式的 H1 标题：
- 禁止 \`# {篇名} 解读\` 这种带"解读"二字的 H1
- 禁止 \`# {篇名} · 解读\` 这种带"· 解读"的 H1
- 禁止 \`# 解读\` 等元自我标签式 H1
- **裸篇名由目录系统推导，interpretation.md 不含 H1**
- 文件应直接以 \`## \` 二级标题起首

## 输出格式

请输出完整 interpretation.md 内容（**不**含 H1 标题，裸篇名由目录系统推导）。
`
}
